#Dada uma string 'python é uma linguagem poderosa', crie uma lista com as palavras separadas.

# Cria uma string com o texto dado na questão.
string = str("python é uma linguagem poderosa")

# Separa as palavras da string com a função 'split' e armazena na lista 'palavras'.
palavras = string.split()

# Imprime a lista com as palavras separadas.
print(palavras)